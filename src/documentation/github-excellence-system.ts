  private async updateREADMEWithBackup(path: string, content: string): Promise<void> {
    // Create backup
    try {
      const existing = await fs.readFile(path, 'utf-8');
      await fs.writeFile(`${path}.backup`, existing);
    } catch (error) {
      // No existing file
    }

    await fs.writeFile(path, content);
  }

